package cn.com.enjoystudy.oa.bean.study;

import java.util.List;

public class StoreQuestion extends Question {
    /** 考卷ID */
    private String paperId;
    /** 解析 */
    private String analysis;
    /** 解题选项 */
    private List<StoreQuestionItem> itemList;

    public StoreQuestion() {
    }

    public String getPaperId() {
        return paperId;
    }

    public void setPaperId(String paperId) {
        this.paperId = paperId;
    }

    public String getAnalysis() {
        return analysis;
    }

    public void setAnalysis(String analysis) {
        this.analysis = analysis;
    }

    public List<StoreQuestionItem> getItemList() {
        return itemList;
    }

    public void setItemList(List<StoreQuestionItem> itemList) {
        this.itemList = itemList;
    }
}
