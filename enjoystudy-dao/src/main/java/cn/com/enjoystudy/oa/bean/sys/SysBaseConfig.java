package cn.com.enjoystudy.oa.bean.sys;

import cn.com.enjoystudy.oa.bean.EntitySupport;

public class SysBaseConfig extends EntitySupport {
    /** 系统名称 */
    private String systemName;
    /** 版本所有 */
    private String copyright;
    /** 网址 */
    private String webSite;
    /** 允许上传的图片扩展名 */
    private String allowUploadImage;
    /** 允许上传的文件扩展名 */
    private String allowUploadFile;
    /** 允许上传的媒体扩展名 */
    private String allowUploadMedia;
    /** 允许上传的FLASH扩展名 */
    private String allowUploadFlash;
    /** 关键字，多个用英文“,”分隔 */
    private String keywords;
    /** 描述，网站（系统描述） */
    private String description;

    public SysBaseConfig() {
    }

    public String getSystemName() {
        return systemName;
    }

    public void setSystemName(String systemName) {
        this.systemName = systemName;
    }

    public String getCopyright() {
        return copyright;
    }

    public void setCopyright(String copyright) {
        this.copyright = copyright;
    }

    public String getWebSite() {
        return webSite;
    }

    public void setWebSite(String webSite) {
        this.webSite = webSite;
    }

    public String getAllowUploadImage() {
        return allowUploadImage;
    }

    public void setAllowUploadImage(String allowUploadImage) {
        this.allowUploadImage = allowUploadImage;
    }

    public String getAllowUploadFile() {
        return allowUploadFile;
    }

    public void setAllowUploadFile(String allowUploadFile) {
        this.allowUploadFile = allowUploadFile;
    }

    public String getAllowUploadMedia() {
        return allowUploadMedia;
    }

    public void setAllowUploadMedia(String allowUploadMedia) {
        this.allowUploadMedia = allowUploadMedia;
    }

    public String getAllowUploadFlash() {
        return allowUploadFlash;
    }

    public void setAllowUploadFlash(String allowUploadFlash) {
        this.allowUploadFlash = allowUploadFlash;
    }

    public String getKeywords() {
        return keywords;
    }

    public void setKeywords(String keywords) {
        this.keywords = keywords;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
