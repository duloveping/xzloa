package cn.com.enjoystudy.oa.bean.base;

import cn.com.enjoystudy.oa.bean.SOSupport;

/**
 * 职务
 * @author 广州新咨力科技有限公司-杜永生
 */
public class EmployeePositionSO extends SOSupport {
    /** 员工ID */
    private String employeeId;
    /** 员工姓名 */
    private String employeeName;
    /** 所属员工 */
    private EmployeeAccount employee;
    /** 职务ID */
    private String positionId;
    /** 职务名称 */
    private String positionName;
    /** 所属职务 */
    private BasePosition position;

    public EmployeePositionSO() {
    }

    public String getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(String employeeId) {
        this.employeeId = employeeId;
    }

    public String getEmployeeName() {
        return employeeName;
    }

    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }

    public EmployeeAccount getEmployee() {
        return employee;
    }

    public void setEmployee(EmployeeAccount employee) {
        this.employee = employee;
    }

    public String getPositionId() {
        return positionId;
    }

    public void setPositionId(String positionId) {
        this.positionId = positionId;
    }

    public String getPositionName() {
        return positionName;
    }

    public void setPositionName(String positionName) {
        this.positionName = positionName;
    }

    public BasePosition getPosition() {
        return position;
    }

    public void setPosition(BasePosition position) {
        this.position = position;
    }
}
